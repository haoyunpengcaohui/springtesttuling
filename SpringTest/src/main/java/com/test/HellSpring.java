package com.test;

public class HellSpring {

	private String name;
	private User user;
	
	
	public HellSpring() {
		super();
	}


	public HellSpring(String name, User user) {
		super();
		this.name = name;
		this.user = user;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public User getUser() {
		return user;
	}


	public void setUser(User user) {
		this.user = user;
	}
	
	

}
